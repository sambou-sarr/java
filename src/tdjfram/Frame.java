    private void ajouterBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterBActionPerformed
        String genererT =generecodebarre();
        DefaultTableModel tableModel = (DefaultTableModel)table.getModel();
        int id=tableModel.getRowCount()+1 ;
        String dispoT;
        if(rdispo.isSelected()){
        dispoT ="oui";
        }else{
           dispoT ="non";
        }
        if(libelleT.getText().equals("") || auteurT.getText().equals("") || dateT.getText().equals("") ){
            JOptionPane.showMessageDialog(this,"formulaire non remplis");
        }else{
        String  ligne[] = {String.valueOf(id),auteurT.getText(),dateT.getText(),genererT,libelleT.getText(),dispoT};
        tableModel.addRow(ligne);
        JOptionPane.showMessageDialog(this,"ligne ajouter");
       auteurT.setText("");
       dateT.setText("");
       libelleT.setText("");

        }
    
    }//GEN-LAST:event_ajouterBActionPerformed

