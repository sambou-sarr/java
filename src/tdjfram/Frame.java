    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        // TODO add your handling code here:
       DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
         
       if(table.getSelectedRowCount() == 1){
           String auteur = auteurT.getText();
            String date = dateT.getText();
            String libelle = libelleT.getText();
           
           String dispo;
           if(rdispo.isSelected()){
          dispo ="oui";
        }else{
           dispo ="non";
        }
           
           tableModel.setValueAt(auteur, table.getSelectedRow(), 1 );
            tableModel.setValueAt(date, table.getSelectedRow(), 2 );
            tableModel.setValueAt(libelle, table.getSelectedRow(), 4 );
            tableModel.setValueAt(dispo, table.getSelectedRow(), 5 );
                   
                JOptionPane.showMessageDialog(this,"ligne modifier");
            
       }else{
           if(table.getRowCount() ==0){
                JOptionPane.showMessageDialog(this,"la table est vide");
            
           }else{
                JOptionPane.showMessageDialog(this,"selectioner la ligne a modifier");
            
           }
       }
        
        
    }//GEN-LAST:event_modifierActionPerformed

